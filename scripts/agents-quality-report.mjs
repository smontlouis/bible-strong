import fs from 'node:fs'
import path from 'node:path'

const repoRoot = process.cwd()
const featuresDir = path.join(repoRoot, 'src/features')
const docsDir = path.join(repoRoot, 'docs/agents')
const scratchDir = path.join(repoRoot, '.scratch/quality')
const markdownPath = path.join(docsDir, 'quality-score.md')
const jsonPath = path.join(scratchDir, 'quality.json')
const checkMode = process.argv.includes('--check')
const minScoreArg = process.argv.find(arg => arg.startsWith('--min-score='))
const minScore = Number(minScoreArg?.split('=')[1] ?? process.env.AGENTS_QUALITY_MIN_SCORE ?? 5)

const sensitiveDomains = new Set(['profile', 'settings', 'onboarding'])
const smokeDomains = new Set([
  'bible',
  'search',
  'settings',
  'onboarding',
  'plans',
  'timeline',
  'lexique',
  'nave',
  'dictionnary',
])

const getFiles = dir => {
  if (!fs.existsSync(dir)) return []

  return fs.readdirSync(dir, { withFileTypes: true }).flatMap(entry => {
    const fullPath = path.join(dir, entry.name)

    if (entry.isDirectory()) {
      if (['node_modules', '.git'].includes(entry.name)) return []
      return getFiles(fullPath)
    }

    return [fullPath]
  })
}

const countMatches = (content, regex) => (content.match(regex) ?? []).length

const scoreDomain = domain => {
  const domainDir = path.join(featuresDir, domain)
  const files = getFiles(domainDir).filter(file => /\.(ts|tsx)$/.test(file))
  const testFiles = files.filter(file => /(__tests__|\.test\.|\.spec\.)/.test(file))
  const sourceFiles = files.filter(file => !testFiles.includes(file))
  const contents = sourceFiles.map(file => fs.readFileSync(file, 'utf8')).join('\n')

  const todoCount = countMatches(contents, /\b(TODO|FIXME)\b/g)
  const anyCount = countMatches(contents, /:\s*any\b|\bas\s+any\b/g)
  const consoleCount = countMatches(contents, /\bconsole\.(log|warn|error|debug|info)\b/g)
  const eslintDisableCount = countMatches(contents, /eslint-disable/g)
  const hasReadme = fs.existsSync(path.join(domainDir, 'README.md'))
  const hasSmokePath = smokeDomains.has(domain)
  const isSensitive = sensitiveDomains.has(domain)

  const penalties = []
  if (testFiles.length === 0) penalties.push({ points: 2, reason: 'no colocated feature tests' })
  if (!hasSmokePath) penalties.push({ points: 1, reason: 'no mapped smoke path' })
  if (!hasReadme) penalties.push({ points: 1, reason: 'no feature README' })
  if (anyCount > 5) penalties.push({ points: 1, reason: `${anyCount} any casts/annotations` })
  if (todoCount > 5) penalties.push({ points: 1, reason: `${todoCount} TODO/FIXME markers` })
  if (consoleCount > 10) penalties.push({ points: 1, reason: `${consoleCount} console calls` })
  if (eslintDisableCount > 0)
    penalties.push({ points: 1, reason: `${eslintDisableCount} eslint-disable markers` })
  if (isSensitive) penalties.push({ points: 1, reason: 'sensitive user/account surface' })

  const score = Math.max(1, 10 - penalties.reduce((sum, penalty) => sum + penalty.points, 0))

  return {
    domain,
    score,
    files: sourceFiles.length,
    tests: testFiles.length,
    hasReadme,
    hasSmokePath,
    sensitive: isSensitive,
    signals: {
      todoCount,
      anyCount,
      consoleCount,
      eslintDisableCount,
    },
    risks: penalties.map(penalty => penalty.reason),
  }
}

const domains = fs
  .readdirSync(featuresDir, { withFileTypes: true })
  .filter(entry => entry.isDirectory())
  .map(entry => entry.name)
  .sort()
  .map(scoreDomain)

const generatedAt = new Date().toISOString()

const renderRisk = domain => {
  if (domain.risks.length === 0) return 'none from static scan'
  return domain.risks.join('; ')
}

const markdown = `# Domain Quality Score

Generated by \`yarn agents:quality\`.

This report is a directional agent-readability score, not a product quality verdict. Use it to choose validation depth and identify domains that need better tests, smoke paths, docs, or log coverage.

## Summary

| Domain | Score | Files | Tests | Smoke | Sensitive | Main Risks |
|---|---:|---:|---:|---|---|---|
${domains
  .map(
    domain =>
      `| \`${domain.domain}\` | ${domain.score}/10 | ${domain.files} | ${domain.tests} | ${
        domain.hasSmokePath ? 'yes' : 'no'
      } | ${domain.sensitive ? 'yes' : 'no'} | ${renderRisk(domain)} |`
  )
  .join('\n')}

## Scoring Inputs

- Starts at 10.
- -2 when no colocated feature tests are found.
- -1 when no smoke path is mapped.
- -1 when the feature has no README.
- -1 when static scan finds many \`any\`, \`TODO/FIXME\`, console calls, or any \`eslint-disable\`.
- -1 for sensitive account/user-data surfaces.

## Agent Guidance

- Scores 8-10: normal validation path is usually enough.
- Scores 5-7: run broader checks and use the smoke checklist when touching user-facing behavior.
- Scores 1-4: inspect domain docs and sensitive-area policy before changing behavior; prefer adding tests or smoke coverage with the change.
`

fs.mkdirSync(docsDir, { recursive: true })
fs.mkdirSync(scratchDir, { recursive: true })
fs.writeFileSync(markdownPath, markdown)
fs.writeFileSync(jsonPath, JSON.stringify({ generatedAt, domains }, null, 2))

console.log(`Wrote ${path.relative(repoRoot, markdownPath)}`)
console.log(`Wrote ${path.relative(repoRoot, jsonPath)}`)

if (checkMode) {
  const failingDomains = domains.filter(domain => domain.score < minScore)

  if (failingDomains.length > 0) {
    console.error(`Domain quality check failed. Minimum score is ${minScore}/10.`)
    for (const domain of failingDomains) {
      console.error(`- ${domain.domain}: ${domain.score}/10 (${renderRisk(domain)})`)
    }
    process.exit(1)
  }

  console.log(`Domain quality check passed. All domains are >= ${minScore}/10.`)
}
