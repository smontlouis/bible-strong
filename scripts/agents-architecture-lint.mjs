import fs from 'node:fs'
import path from 'node:path'

const repoRoot = process.cwd()
const srcDir = path.join(repoRoot, 'src')
const docsDir = path.join(repoRoot, 'docs/agents')
const scratchDir = path.join(repoRoot, '.scratch/architecture')
const markdownPath = path.join(docsDir, 'architecture-lint.md')
const jsonPath = path.join(scratchDir, 'architecture.json')
const checkMode = process.argv.includes('--check')

const sourceExtensions = new Set(['.ts', '.tsx'])
const ignoredDirs = new Set(['node_modules', '.git'])

const allowedExpoSqliteFiles = new Set([
  'src/helpers/sqlite.ts',
  'src/helpers/biblesDb.ts',
  'src/helpers/getSQLTransaction.ts',
])

const getFiles = dir => {
  if (!fs.existsSync(dir)) return []

  return fs.readdirSync(dir, { withFileTypes: true }).flatMap(entry => {
    if (ignoredDirs.has(entry.name)) return []

    const fullPath = path.join(dir, entry.name)
    if (entry.isDirectory()) return getFiles(fullPath)
    if (!sourceExtensions.has(path.extname(entry.name))) return []

    return [fullPath]
  })
}

const normalize = file => path.relative(repoRoot, file).split(path.sep).join('/')

const addFinding = (findings, severity, rule, file, line, message) => {
  findings.push({
    severity,
    rule,
    file: normalize(file),
    line,
    message,
  })
}

const getLineNumber = (content, index) => content.slice(0, index).split('\n').length

const importRegex =
  /(?:import\s+(?:type\s+)?(?:[^'"]+\s+from\s+)?|export\s+(?:type\s+)?[^'"]+\s+from\s+)['"]([^'"]+)['"]/g

const findings = []

for (const file of getFiles(srcDir)) {
  const relativeFile = normalize(file)
  const content = fs.readFileSync(file, 'utf8')

  for (const match of content.matchAll(importRegex)) {
    const imported = match[1]
    const line = getLineNumber(content, match.index ?? 0)

    if (imported === 'expo-sqlite' && !allowedExpoSqliteFiles.has(relativeFile)) {
      addFinding(
        findings,
        'error',
        'sqlite-boundary',
        file,
        line,
        'Import expo-sqlite through the database helper boundary instead of directly.'
      )
    }

    if (relativeFile.startsWith('src/helpers/') && imported.startsWith('~features/')) {
      addFinding(
        findings,
        'warning',
        'helpers-feature-boundary',
        file,
        line,
        'Shared helpers must not depend on feature modules.'
      )
    }

    if (
      relativeFile.startsWith('src/features/') &&
      (imported.startsWith('@react-native-firebase/') || imported === '~helpers/firebase')
    ) {
      addFinding(
        findings,
        'warning',
        'feature-firebase-boundary',
        file,
        line,
        'Feature code should prefer a local helper/hook boundary over direct Firebase access.'
      )
    }

    if (relativeFile.startsWith('src/features/') && /^(\.\.\/){3,}/.test(imported)) {
      addFinding(
        findings,
        'warning',
        'deep-relative-import',
        file,
        line,
        'Prefer path aliases or a small public module boundary over deep relative imports.'
      )
    }
  }

  for (const match of content.matchAll(/\bconsole\.(log|warn|error|debug|info)\b/g)) {
    const line = getLineNumber(content, match.index ?? 0)

    if (relativeFile !== 'src/helpers/agentObservability.ts') {
      addFinding(
        findings,
        'warning',
        'raw-console',
        file,
        line,
        'Prefer appLogger for app-owned diagnostic events that agents should query.'
      )
    }
  }
}

const bySeverity = findings.reduce(
  (acc, finding) => {
    acc[finding.severity] += 1
    return acc
  },
  { error: 0, warning: 0 }
)

const byRule = findings.reduce((acc, finding) => {
  acc[finding.rule] = (acc[finding.rule] ?? 0) + 1
  return acc
}, {})

const generatedAt = new Date().toISOString()
const markdown = `# Architecture Lint

Generated by \`yarn agents:architecture\`.

This is a repo-specific architecture scan for agent work. It is intentionally conservative: errors are CI-blocking boundaries, warnings describe brownfield debt or places where agents should be careful.

## Summary

- Errors: ${bySeverity.error}
- Warnings: ${bySeverity.warning}

## Rules

| Rule | Severity | Findings | Meaning |
|---|---|---:|---|
| \`sqlite-boundary\` | error | ${byRule['sqlite-boundary'] ?? 0} | Import SQLite through helper boundaries. |
| \`helpers-feature-boundary\` | warning | ${byRule['helpers-feature-boundary'] ?? 0} | Shared helpers should not depend on features. |
| \`feature-firebase-boundary\` | warning | ${byRule['feature-firebase-boundary'] ?? 0} | Feature code should avoid direct Firebase access. |
| \`deep-relative-import\` | warning | ${byRule['deep-relative-import'] ?? 0} | Prefer aliases or public module boundaries over deep relative imports. |
| \`raw-console\` | warning | ${byRule['raw-console'] ?? 0} | Prefer \`appLogger\` for agent-queryable diagnostics. |

## Findings

${
  findings.length === 0
    ? 'No architecture findings.'
    : findings
        .map(
          finding =>
            `- ${finding.severity.toUpperCase()} \`${finding.rule}\` ${finding.file}:${finding.line} - ${finding.message}`
        )
        .join('\n')
}
`

fs.mkdirSync(docsDir, { recursive: true })
fs.mkdirSync(scratchDir, { recursive: true })
fs.writeFileSync(markdownPath, markdown)
fs.writeFileSync(
  jsonPath,
  JSON.stringify({ generatedAt, summary: bySeverity, byRule, findings }, null, 2)
)

console.log(`Wrote ${path.relative(repoRoot, markdownPath)}`)
console.log(`Wrote ${path.relative(repoRoot, jsonPath)}`)

if (checkMode) {
  if (bySeverity.error > 0) {
    console.error(`Architecture check failed with ${bySeverity.error} error(s).`)
    for (const finding of findings.filter(item => item.severity === 'error')) {
      console.error(`- ${finding.rule} ${finding.file}:${finding.line} ${finding.message}`)
    }
    process.exit(1)
  }

  console.log(`Architecture check passed with ${bySeverity.warning} warning(s).`)
}
